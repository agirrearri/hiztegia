<p>Goitizenetatik aparte, herrian pertsona bat nongoa den adierazteko aspalditik erabili izan dira txalupa izenak, eta afimilien abizenak &quot;-<em>NEKU&quot; </em>edo<em> &quot;-KU&quot; </em>atzizkia lagun dutela: <em>Mirentxuku</em> (<em>Mirentxu</em> txalupa izan zuen familiakoa), <em>Bustioku </em>(txalupa: <em>Bustio</em>) <em>Sagradaku</em>, <em>Solabarrietaku</em>, <em>Ituarteneku</em>, <em>Usandizaganeku</em>.</p>

<p>Kasu batzuetan zer gertatu da? Pertsona batek familiaz goitizena izan arren, berari asmatu diotela beste bat. Beraz baziren goitizen bat baino gehiago zutenak. Paulo Markuerkiagari (Domeka <em>Tobaneku</em>ren aitari), Paulo <em>&quot;Toba&quot; </em>deitzen zioten, baina, Paulo <em>&quot;Papardo&quot; </em>ere bai, papardoak harrapatzen abilla zelako, nahiz eta <em>&quot;Papardo&quot; </em>familiakoa ez izan. Jose Etxaburu Barbariasek berriz hiru daramatza: <em>Astilleruko Koju, Jose Kutxi, </em>eta<em> Talaxeruneku. </em></p>

